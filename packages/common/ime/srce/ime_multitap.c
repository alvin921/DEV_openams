static void	multitap_timeout(int timerid, void *arg)
{
	t_IMEMultitap *pme = arg;
	if(pme && pme->timer_id>=0){
		pme->key = MVK_NONE;
		pme->tab_index = 0;
		//3 unselect the text
		ime_agent_unselect_char();
	}
}


/*
multitap输入法规则
LANG_123:
	PRESS和REPEAT都是输入数字, UP/DOWN/LONG_PRESS忽略
LANG_xxx:
	LONG_PRESS: 输入数字
	PRESS: 在同组键值间切换
	DOWN/UP/REPEAT: 无效
*/
#define REPEAT_TIMEOUT		800


static void	IME_Multitap_OnConfig(t_HWindow handle, t_IMEMultitap *wdata, t_sparam sparam, t_lparam lparam)
{
		gu8 s = 0xff;
		int idx;

		switch(sparam){
		case LANG_123:
			if(lparam != (t_lparam)(-1))
				wdata->digit_type = lparam;	//2  DIGIT_TYPE_XXXX
			wdata->lang = sparam;
			wdata->tab = NULL;
			break;
		default:
			if(sparam >= LANG_MAX)
				break;
			for(idx=0; idx < ARRAY_SIZE(gaMultitapIMESupported); idx++){
				if(gaMultitapIMESupported[idx].lang == wdata->lang){
					wdata->idx_ime = idx;
					wdata->lang = sparam;
					wdata->tab = NULL;
					wdata->idx_ime = -1;
					if(lparam != (t_lparam)(-1))
						wdata->case_type = lparam;	//2 IME_CASE_UPPER
					wdata->tab = gaMultitaps[wdata->lang].lower_tab;
					if(lparam == IME_CASE_UPPER && gaMultitaps[wdata->lang].upper_tab){
						wdata->tab = gaMultitaps[wdata->lang].upper_tab;
					}
					break;
				}
			}
			break;
		}
}

static error_t	IME_Multitap_WndMsgHandler(t_HWindow handle, t_WidgetEvent evt, t_sparam sparam, t_lparam lparam)
{
	error_t ret = NOT_HANDLED;
	t_IMEMultitap *wdata = (t_IMEMultitap *)wgt_get_tag(handle);
	
	switch(evt){
		case WIDGET_OnMessage:
			{
				t_AmosMsg *msg = (t_AmosMsg *)lparam;
				switch(sparam){
					case EVT_IME_CONFIG:
						IME_Multitap_OnConfig(handle, wdata, msg->sparam, msg->lparam);
						ret = SUCCESS;
						break;
				}
			}
			break;
		case WINDOW_OnOpen:
			g_printf("WINDOW_OnOpen\r\n");
			wdata = MALLOC(sizeof(t_IMEMultitap));
			memset(wdata, 0, sizeof(t_IMEMultitap));
			wgt_set_tag(handle, wdata);

			wdata->idx_ime = 0;
			wdata->lang = LANG_123;
			wdata->key = MVK_NONE;
			wdata->case_type = IME_CASE_NONE;
			wdata->tab_index = 0;
			wdata->timer_id = timer_create(multitap_timeout, wdata);

			IME_Multitap_OnConfig(handle, wdata, LANG_123, DIGIT_TYPE_NUMBER);
			wgt_set_bound((t_HWidget)handle, 0, 0, lcd_get_width(), 0);
			break;
		case WINDOW_OnClose:
			g_printf("WINDOW_OnClose\r\n");
			if(wdata){
				if(wdata->timer_id)
					timer_release(wdata->timer_id);
				FREE(wdata);
			}
			ret = SUCCESS;
			break;
		case WIDGET_OnKeyEvent:
			if(MVK_IS_DIGIT(lparam)){
				if(wdata->lang == LANG_123){
					switch(lparam){
						case MVK_STAR:
							if(wdata->digit_type == DIGIT_TYPE_PHONE){
								if(sparam == MMI_KEY_DOWN)
									ime_agent_add_char(IME_ADD_MODE_SELECT, '*');
								else if(sparam == MMI_KEY_LONG_PRESS)
									ime_agent_add_char(IME_ADD_MODE_NORMAL, 'p');
								else if(sparam == MMI_KEY_UP)
									ime_agent_unselect_char();
							}else{
								if(sparam == MMI_KEY_PRESS||sparam == MMI_KEY_REPEAT)
									ime_agent_add_char(IME_ADD_MODE_NORMAL, '*');
							}
							break;
						case MVK_HASH:
							if(wdata->digit_type == DIGIT_TYPE_PHONE){
								if(sparam == MMI_KEY_DOWN)
									ime_agent_add_char(IME_ADD_MODE_SELECT, '#');
								else if(sparam == MMI_KEY_LONG_PRESS)
									ime_agent_add_char(IME_ADD_MODE_NORMAL, 'w');
								else if(sparam == MMI_KEY_UP)
									ime_agent_unselect_char();
							}else{
								if(sparam == MMI_KEY_PRESS||sparam == MMI_KEY_REPEAT)
									ime_agent_add_char(IME_ADD_MODE_NORMAL, '#');
							}
							break;
						case MVK_0:
							if(sparam == MMI_KEY_DOWN)
								ime_agent_add_char(IME_ADD_MODE_SELECT, '0');
							else if(sparam == MMI_KEY_LONG_PRESS)
								ime_agent_add_char(IME_ADD_MODE_NORMAL, '+');
							else if(sparam == MMI_KEY_UP)
								ime_agent_unselect_char();
							break;
						default:
							if(sparam == MMI_KEY_PRESS || sparam == MMI_KEY_REPEAT){
								ime_agent_add_char(IME_ADD_MODE_NORMAL, '0'+lparam-MVK_0);
							}
							break;
					}
					wdata->tab_index = 0;
					wdata->key = MVK_NONE;
				}else if(wdata->tab){
					if(sparam == MMI_KEY_DOWN){
						timer_stop(wdata->timer_id);
						if(wdata->key!=MVK_NONE && lparam != wdata->key){
							ime_agent_unselect_char();
							wdata->tab_index = 0;
						}
					}else if(sparam == MMI_KEY_LONG_PRESS){
						if(wdata->key == lparam){
							ime_agent_unselect_char();
						}
						ime_agent_add_char(IME_ADD_MODE_NORMAL, '0'+lparam-MVK_0);
						wdata->key = MVK_NONE;
						wdata->tab_index = 0;
					}else if(sparam == MMI_KEY_PRESS/*||sparam == MMI_KEY_REPEAT*/){
						timer_stop(wdata->timer_id);
						timer_start(wdata->timer_id, REPEAT_TIMEOUT, TIMER_MODE_ONCE);
						ime_agent_add_char(IME_ADD_MODE_SELECT, wdata->tab->array[lparam-MVK_0][wdata->tab_index]);
						//IMEAgent_Notify_Select(handle, -1, 1);
						wdata->key = lparam;
						wdata->tab_index++;
						if(wdata->tab->array[lparam-MVK_0][wdata->tab_index] == 0)
							wdata->tab_index = 0;
					}
				}
				ret = SUCCESS;
			}else{
				t_HWindow wnd = wgt_parent_window(ime_get_attached_widget());
				wgt_send_msg(wnd, EVT_KEY, sparam, lparam, 0);
				ret = SUCCESS;
			}
			break;
		case WINDOW_OnDraw:
			//__ShowIME(handle, TRUE);
			ret = SUCCESS;
			break;
	}
	return ret;
}
