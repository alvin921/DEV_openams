void		pbx_hst_reset(pbx_t *pbx)
{
	if(pbx){
		pbx->hst_front = 0;
		pbx->hst_tail = 0;
		pbx->hst_cur = ACT_HST_MAX-1;

		pbx->push = 0;
		pbx->step = 0;
	}
}

#define	pbx_hst_empty(pbx)		((pbx)->hst_front == (pbx)->hst_tail)
#define	pbx_hst_full(pbx)	(((pbx)->hst_front+ACT_HST_MAX-(pbx)->hst_tail)%ACT_HST_MAX == 1)
#define	pbx_hst_meet_front(pbx)	(((pbx)->hst_front + ACT_HST_MAX- (pbx)->hst_cur)%ACT_HST_MAX == 1)
#define	pbx_hst_meet_tail(pbx)	((pbx->hst_tail + ACT_HST_MAX- pbx->hst_cur)%ACT_HST_MAX == 1)

pbx_action_t*	pbx_hst_current_action(pbx_t *pbx)
{
	return (!pbx_hst_empty(pbx) && !pbx_hst_meet_front(pbx))? &pbx->history[pbx->hst_cur] : NULL;
}

pbx_action_t*	pbx_hst_next_action(pbx_t *pbx)
{
	if(!pbx_hst_empty(pbx) && !pbx_hst_meet_tail(pbx)){
		pbx->hst_cur = (pbx->hst_cur + 1)%ACT_HST_MAX;
		return &pbx->history[pbx->hst_cur];
	}
	return NULL;
}

void		pbx_hst_enqueue(pbx_t *pbx, gu8 type, t_MapIndex prow, t_MapIndex pcol)
{
	if(pbx){
		pbx_action_t *act;
		act = pbx_hst_current_action(pbx);
		if(act == NULL){
			pbx->hst_cur = pbx->hst_front;
		}else{
			pbx->hst_cur = (pbx->hst_cur+1)%ACT_HST_MAX;
		}
		pbx->history[pbx->hst_cur].type = type;
		pbx->history[pbx->hst_cur].prow = prow;
		pbx->history[pbx->hst_cur].pcol = pcol;
		pbx->hst_tail = (pbx->hst_cur+1)%ACT_HST_MAX;
		if(pbx_hst_full(pbx))
			pbx->hst_front = (pbx->hst_front+1)%ACT_HST_MAX;
	}
}

