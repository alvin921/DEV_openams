gboolean		pbx_map_move_to(pbx_t *pbx, t_MapIndex row, t_MapIndex col, gu8 mode)
{
	pbx_map_t	*map;
	gu8 pane;
	t_MapIndex p_row, p_col;
	t_MapIndex dir;
	gu8 *path = NULL;
	gs8 path_len = 0, i;
	
	if(pbx_map_success(pbx))
		return FALSE;
	if(pbx && (row >= 0) && (row < MAP_ROW_MAX) && (col >= 0) && (col < MAP_COL_MAX)){
		map = &pbx->map;
		p_row = map->porter_row;
		p_col = map->porter_col;
		if((p_col == col) && (p_row == row))return FALSE;
		
		pane = MAP_ROLE(map, row, col);
		if(map_cell_adjacent(p_row, p_col, row, col, &dir)){
			return pbx_map_move(pbx, dir, mode);
		}
		if(PANE_IS_FLOOR(pane)){
			path_len = pbx_map_find_path(pbx, p_row, p_col, row, col, &path);
			if(path_len > 0){
				for(i = path_len-1; i >= 0; i--){
					if(pane_porter_action(pbx, map->porter_row, map->porter_col, path[i])){
						if(mode == ACT_UNDO)
							pbx->step--;
						else
							pbx->step++;
					}
					amos_sleep(DEMO_TIMEOUT);
				}
				if((mode == ACT_NORMAL) && (path_len > 0))
					pbx_hst_enqueue(pbx, ACT_MOVE, p_row, p_col);
				pbx_show_action_info(pbx->window, pbx, FALSE);
				FREE(path);
			}
		}
	}
	return path_len>0?TRUE:FALSE;
}

