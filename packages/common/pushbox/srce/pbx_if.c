static int	__shortcut_compare(const void *a1, const void *a2)
{
	helt_shortcut_t * e1 = (helt_shortcut_t *)a1;
	helt_shortcut_t * e2 = (helt_shortcut_t *)a2;
	if((e1->row == e2->row) && (e1->col == e2->col) && (e1->direction == e2->direction))
		return 0;
	if(e1->row < e2->row)
		return -1;
	return 1;
}
static void	__shortcut_free(void *data)
{
	FREE(data);
}
static int	__shortcut_calculate(void *data, astar_node_t *node)
{
	t_AstarShortcut *pme = data;
	helt_shortcut_t *elt = (helt_shortcut_t *)node;
	return (int)(DIST(elt->row, elt->col, pme->row2, pme->col2));
}

static astar_node_t * __shortcut_expand(void *data, astar_node_t *node)
{
	t_AstarShortcut *pme = data;
	helt_shortcut_t *elt = (helt_shortcut_t *)node;
	gu8 d;
	t_MapIndex row, col;

	helt_shortcut_t * exnode;

	exnode = NULL;
	if(node){
		helt_shortcut_t *subnode;
		for(d = 0; d < 4; d++){
			if(map_next_cell(pme->map, elt->row, elt->col, &row, &col, DIRECTION(d)) == FALSE)
				continue;
			if(!PANE_MOVABLE(MAP_ROLE(pme->map, row, col)))
				continue;
			subnode = (helt_shortcut_t *)MALLOC(sizeof(helt_shortcut_t));
			if(subnode == NULL)break;
			memset(subnode, 0, sizeof(helt_shortcut_t));
			subnode->row = row;
			subnode->col = col;
			subnode->direction = DIRECTION(d);
			subnode->node.forward = (astar_node_t*)exnode;
			exnode = subnode;
		}
	}else{
		exnode = (helt_shortcut_t *)MALLOC(sizeof(helt_shortcut_t));
		if(exnode){
			memset(exnode, 0, sizeof(helt_shortcut_t));
			exnode->row = pme->row;
			exnode->col = pme->col;
			exnode->direction = guiNoDir;
			exnode->node.g = 0;
			exnode->node.forward = NULL;
		}
	}
	return (astar_node_t*)exnode;
}

static int	__shortcut_travel(void *data, astar_node_t *node)
{
	t_AstarShortcut *pme = data;
	helt_shortcut_t *elt = (helt_shortcut_t *)node;
	if(pme->path_len >= pme->path_size){
		gu8 *new_path;
		if(pme->path_size == 0)
			pme->path_size = 8;
		else
			pme->path_size <<= 1;
		new_path = MALLOC(pme->path_size);
		if(pme->path){
			memcpy(new_path, pme->path, pme->path_len);
			FREE(pme->path);
		}
		pme->path = new_path;
	}
	pme->path[pme->path_len++] = elt->direction;
	return 0;
}

