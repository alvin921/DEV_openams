static void pbx_show_stage_info(t_HWindow handle, pbx_t *pbx, gboolean defer)
{
	char str[20];
	t_HGDI gdi = wgt_get_gdi(handle);
	int th = wnd_get_status_bar_height(handle);
	int tw;
	coord_t x, y;

	gdi_measure_text(gdi, wgt_get_caption(handle), -1, CHARSET_UTF8, -1, &tw, NULL, NULL);
	x = tw+2;
	y = 0;
	sprintf(str, "-%s: %d/%d", brd_get_string(brd_get(PACK_GUID_SELF), RESID_STR_STAGE, ""), pbx->stage_cur+1, pbx->stage_num);
	gdi_measure_text(gdi, str, -1, CHARSET_UTF8, -1, &tw, NULL, NULL);

	if(!defer)
		gdi_clear(gdi, x, y, tw+10, th, guiFourCorner);
	gdi_set_color(gdi, ui_theme_get()->fg_color);
	gdi_draw_text_in(gdi, x, y, tw, th, str, -1, CHARSET_UTF8, IDF_HALIGN_LEFT|IDF_VALIGN_MIDDLE);
	if(!defer)
		gdi_blt(gdi, x, y, tw+10, th);
	g_object_unref(gdi);
}
static void pbx_show_action_info(t_HWindow handle, pbx_t *pbx, gboolean defer)
{
	char str[20];
	t_HWidget sk = wnd_get_skbar(handle);
	t_HGDI gdi = wgt_get_client_gdi(sk);
	gui_rect_t client;
	
	sprintf(str, "%s: %d/%d", brd_get_string(brd_get(PACK_GUID_SELF), RESID_STR_ACTION, ""), pbx->push, pbx->step);
	wgt_get_client_rect(sk, &client);
	if(!defer)
		gdi_clear(gdi, client.x+client.dx/3, 0, client.dx/3, client.dy, guiFourCorner);
	gdi_set_color(gdi, ui_theme_get()->fg_color);
	gdi_draw_text_in(gdi, client.x+client.dx/3, 0, client.dx/3, client.dy, str, -1, CHARSET_UTF8, IDF_HALIGN_LEFT|IDF_VALIGN_MIDDLE);
	if(!defer)
		gdi_blt(gdi, client.x+client.dx/3, 0, client.dx/3, client.dy);
	g_object_unref(gdi);
}

