int		pbx_map_find_path(pbx_t *pbx, t_MapIndex row, t_MapIndex col, t_MapIndex row2, t_MapIndex col2, gu8 **path)
{
	t_AstarShortcut sc;

	if((row == row2) && (col == col2)){
		return 0;
	}

	sc.map = &pbx->map;
	sc.row = row;
	sc.col = col;
	sc.row2 = row2;
	sc.col2 = col2;
	sc.path = NULL;
	sc.path_size = 0;
	sc.path_len = 0;

	astar_run(&sc, __shortcut_compare, __shortcut_free, __shortcut_calculate, __shortcut_expand, __shortcut_travel);
	if(path)*path = sc.path;
	else FREEIF(sc.path);
	return sc.path_len;
}

